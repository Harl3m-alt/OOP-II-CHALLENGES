public class Main {
    public static void main(String[] args) {
        // Declare a variable
        int number = -3;

        // Use if-else to check the value
        if (number > 0) {
            System.out.println("The number is positive.");
        } else if (number < 0) {
            System.out.println("The number is negative.");
        } else {
            System.out.println("The number is zero.");
        }
    }
}
